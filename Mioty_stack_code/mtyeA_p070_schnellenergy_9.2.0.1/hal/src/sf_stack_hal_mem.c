#include <stdint.h>
#include "sf_stack_hal_mem.h"

/*==============================================================================
                               MACROS
==============================================================================*/
#ifndef MEM_SHARED_RAM_SIZE
  #define MEM_SHARED_RAM_SIZE           0x800
#endif

/*==============================================================================
                           GLOBAL VARIABLES
==============================================================================*/
static uint8_t gRam[MEM_SHARED_RAM_SIZE];

/*----------------------------------------------------------------------------*/
/*! sf_stack_hal_mem_getRAM() */
/*----------------------------------------------------------------------------*/
void* sf_stack_hal_mem_getRAM( uint32_t* size )
{
  *size = MEM_SHARED_RAM_SIZE;
  return gRam;
}/* sf_stack_hal_mem_getRAM */
